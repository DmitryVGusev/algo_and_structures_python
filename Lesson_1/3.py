# 3.	По введенным пользователем координатам двух точек вывести
# уравнение прямой вида y = kx + b, проходящей через эти точки.
def line_formula():
    """Выводит уравнение вида y = kx + b """
    x1 = int(input("x1: "))
    y1 = int(input("y1: "))
    x2 = int(input("x2: "))
    y2 = int(input("y2: "))

    k = (y1 - y2)/(x1 - x2)
    b = (x1*y2 - x2*y1)/(x1 - x2)

    print(f"Уравнение прямой: y= ({k})x + ({b})")


if __name__ == '__main__':
    line_formula()
